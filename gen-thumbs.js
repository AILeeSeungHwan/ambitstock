function svg(bg, t1, t2, t3) {
  const sz = (t) => {
    const l = t.length;
    if (l <= 4) return 120;
    if (l <= 6) return 105;
    if (l <= 8) return 95;
    if (l <= 10) return 85;
    if (l <= 13) return 75;
    return 65;
  };
  const line = (text, y, fill, stroke, sw) =>
    `  <text x="600" y="${y}" text-anchor="middle" font-family="'Apple SD Gothic Neo','Noto Sans KR',sans-serif" font-size="${sz(text)}" font-weight="900" fill="${fill}" stroke="${stroke}" stroke-width="${sw}" paint-order="stroke">${text}</text>`;
  return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 1200 630">
  <rect width="1200" height="630" fill="${bg}"/>
${line(t1, 185, '#ffffff', '#333333', 5)}
${line(t2, 365, '#FFD700', '#333333', 7)}
${line(t3, 530, '#ffffff', '#333333', 5)}
</svg>`;
}

